.data
A: .word 7  # 設定A的初始值
B: .word 9 # 設定B的初始值
ans: .word 0 # 用來存放計算結果

.text
.globl main
main:
    lw $s0, A     # 載入A到寄存器 $s0
    lw $s1, B     # 載入B到寄存器 $s1
    
    # 計算 A * 6
    li $t0, 6     # 將 6 加載到暫存寄存器 $t0
    mul $s0, $s0, $t0  # 將 $s0 乘以 $t0，結果存放在 $s0

    # 計算 A * 6 + 8
    li $t1, 8     # 將 8 加載到暫存寄存器 $t1
    add $s0, $s0, $t1  # 將 $s0 和 $t1 相加，結果存放在 $s0

    # 計算 B / 2
    li $t2, 2     # 將 2 加載到暫存寄存器 $t2
    div $s1, $t2   # 將 $s1 除以 $t2，商存放在 $s1，餘數存放在 HI 寄存器
    mflo $s1      # 將商複製回 $s1

    # 計算 (A * 6 + 8) * (B / 2)
    mul $s3, $s0, $s1  # 將 $s0 和 $s1 相乘，結果存放在 $s3

    # 輸出 $s3 的值
    move $a0, $s3  # 將 $s3 複製到 $a0，準備輸出
    li $v0, 1      # 載入系統調用代碼1，表示輸出整數
    syscall

    # 結束程式
    li $v0, 10    # 載入系統調用代碼 10，表示結束程式
    syscall
